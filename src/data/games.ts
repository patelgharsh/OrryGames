export const CATEGORIES = ['All', ...Array.from(new Set(GAMES_LIST.map(game => game.category))).sort()];

export function getGameById(id: string): Game | undefined {
  return GAMES_LIST.find(game => game.id === id);
}

export function getGamesByCategory(category: string): Game[] {
  if (!category || category === 'All') return GAMES_LIST;
  return GAMES_LIST.filter(game => game.category === category);
}

export function searchGames(query: string): Game[] {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) return GAMES_LIST;

  return GAMES_LIST.filter(game =>
    game.title.toLowerCase().includes(lowerQuery) ||
    game.category.toLowerCase().includes(lowerQuery) ||
    game.description.toLowerCase().includes(lowerQuery) ||
    game.tags.toLowerCase().includes(lowerQuery)
  );
}
